    #region IDBFunction Interface
    public interface IDBFunction
    {
        void Insert();
        void Update();
        void Delete();

        void Show();

    }
    #endregion IDBFunction Interface

    #region Class Employee
    public abstract class Employee
    {
        #region Property
        //string Name -> no blanks
        protected string name;
        public string Name
        {
            set
            {
                if (value != null)
                {
                    name = value;
                }
                else
                {
                    Console.WriteLine("Invalid Input");
                }
            }
            get
            {
                return name;
            }
        }
        #endregion Property

        #region Property
        //int EmpNo -> must be readonly and autogenerated
        static protected int lastEmpNo = 0;
        protected int empNo;
        public int EmpNo
        {
            get
            {
                return empNo;
            }
        }
        #endregion Property

        #region Property
        //decimal Basic -> must be between some range
        protected decimal basics;
        public abstract decimal Basics
        {
            set;
            get;
        }
        #endregion Property

        #region Property
        //short DeptNo -> must be > 0
        protected short deptNo;
        public short DeptNo
        {
            set
            {
                if (value > 0)
                {
                    deptNo = value;
                }
                else
                {
                    Console.WriteLine("Invalid Input");
                }
            }
            get
            {
                return deptNo;
            }
        }
        #endregion Property

        #region Constructor
        public Employee(string nm = null, decimal bs = 0, short dp = 0)
        {
            name = nm;
            basics = bs;
            deptNo = dp;
            empNo = lastEmpNo++;
        }
        #endregion Constructor

        #region Abstract Method
        public abstract decimal CalcNetSalary();
        #endregion Abstract Method

